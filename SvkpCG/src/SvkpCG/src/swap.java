
public class swap {


			public static void main(String[] args) {
		    int x=20,y=70;
		    System.out.println("Before swap:");
		    System.out.println("x="+x);
		    System.out.println("y="+y);
		    
		    int temp=x;
		    temp=x;
		    x=y;
		    y=temp;
		    System.out.println("Before swap");
		    System.out.println("x="+x);
		    System.out.println("y="+y);

	}

}
